# String Concatenation
# To concatenate, or combine, two strings you can use the + operator.

print("Hello" + "World!")

# This separates the two strings with a space
print("Hello" + " " + "World!")
print("Hello" + " " + "Everyone!," + " " + "it is time to leave")


# Concatenate strings using variables
name = "John"
print("Hello " + name + "!")
# The + operator can be used to concatenate two strings.
